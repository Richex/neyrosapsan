triggers = ['—é–ª–∏—è —Å–∞–º–æ–π–ª–æ–≤–∞', '@—é–ª–∏—è—Å–∞–º–æ–π–ª–æ–≤–∞', '—é–ª–∏—è —Å–∞–ø—Å–∞–Ω', '@—é–ª–∏—è—Å–∞–ø—Å–∞–Ω', '@neyrosapsan_bot', '–Ω–µ–π—Ä–æ —Å–∞–ø—Å–∞–Ω']
triggers_shtosh = ['—à—Ç–æ—à']


@bot.message_handler(commands=['start'])
def start(message):
    bot.send_message(message.chat.id, random.choice(start_text))


@bot.message_handler()
def handle_message(message):
    # –æ—Ç–≤–µ—Ç –±–æ—Ç–∞ –Ω–∞ –ª—é–±–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –≤ –ª–∏—á–Ω–æ–º —á–∞—Ç–µ
    if message.chat.type == "private":
        bot.send_message(message.chat.id, random.choice(common_text))

    elif message.chat.type == "group" or message.chat.type == "supergroup" or message.chat.type == "channel":
        # –ø—Ä–æ–≤–µ—Ä–∫–∞ —É–ø–æ–º–∏–Ω–∞–Ω–∏—è –∏–º–µ–Ω–∏ –±–æ—Ç–∞ –∏ –æ—Ç–ø—Ä–∞–≤–∫–∞ —Å–ª—É—á–∞–π–Ω–æ–≥–æ –æ—Ç–≤–µ—Ç–∞ –∏–∑ –º–∞—Å—Å–∏–≤–∞ common_text
        for trigger in triggers:
            if trigger in message.text.lower():
                bot.send_message(message.chat.id, random.choice(common_text))

        # –ø—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –∫–ª—é—á–µ–≤–æ–µ —Å–ª–æ–≤–æ "–∂–∞–ª—å" –∏ –æ—Ç–ø—Ä–∞–≤–∫–∞ —Å–ª—É—á–∞–π–Ω–æ–≥–æ –æ—Ç–≤–µ—Ç–∞ –∏–∑ –º–∞—Å—Å–∏–≤–∞ uncommon_text
        for shtosh in triggers_shtosh:
            chance = random.randint(17, 36)
            trigger_chance = random.randint(0, 100)
            if shtosh in message.text.lower() and trigger_chance <= chance:
                bot.send_message(message.chat.id, random.choice(shtosh_text))


bot.polling(none_stop=True)
